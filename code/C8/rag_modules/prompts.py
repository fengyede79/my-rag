def build_targeted_prompt(content_type: str = None) -> ChatPromptTemplate:
    """根据内容类型构建针对性的 prompt。

    Args:
        content_type: 内容类型（ingredients/steps/tips/calculation/introduction）

    Returns:
        定制化的 ChatPromptTemplate
    """
    if content_type and content_type in CONTENT_TYPE_TEMPLATES:
        return ChatPromptTemplate.from_template(CONTENT_TYPE_TEMPLATES[content_type])

    return ChatPromptTemplate.from_template(DEFAULT_FULL_PROMPT_TEMPLATE)


def build_no_context_answer(
    query: str,
    content_type: str = None,
    dish_name: str = None,
) -> str:
    """在上下文不足时给出保守回答。

    Args:
        query: 用户查询
        content_type: 内容类型
        dish_name: 从查询中提取的菜品名（由调用方传入）

    Returns:
        保守回答文本
    """
    target_name = dish_name or "这道菜"
    content_type_labels = {
        "ingredients": "食材信息",
        "steps": "制作步骤",
        "tips": "技巧和注意事项",
        "calculation": "用量计算信息",
        "introduction": "菜品介绍",
    }
    content_label = content_type_labels.get(content_type, "完整食谱信息")
    return (
        f'抱歉，知识库里暂时没有足够完整的“{target_name}”{content_label}，'
        '所以我先不编造答案。您可以换一种问法，或先确认这道菜是否已经收录。'
    )
